#Оператор цикла for
for i in range(5):
    print("Hello")#В результате “Hello” будет выведено пять раз
lst = [1, 3, 5, 7, 9]
for i in lst:
    print(i ** 2) #Также можно пройти по всем буквам в строке.
word_str = "Hello, world!"
for l in word_str:
    print(l) #Строка “Hello, world!” будет напечатана в столбик.

